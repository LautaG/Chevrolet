function diseno_show() {
    document.getElementById("diseno_img").style.display = "none";
    document.getElementById("diseno_show").style.display = "none";
    document.getElementById("diseno_overlay").style.display = "block";
    document.getElementById("diseno_back").style.display = "block";
};

function diseno_back() {
    document.getElementById("diseno_img").style.display = "block";
    document.getElementById("diseno_show").style.display = "block";
    document.getElementById("diseno_overlay").style.display = "none";
    document.getElementById("diseno_back").style.display = "none";
};

